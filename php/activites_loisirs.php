Envie de vacances ? Participez aux camps en tant que bénévole
Nous engageons chaque année une centaine de jeunes bénévoles pour aider les campeurs durant les séjours de vacances organisés par l’ABP. Vous aussi venez nous rejoindre si le cœur vous en dit !

Tous ces camps ne seraient pas possibles sans les nombreux volontaires qui viennent aider les campeurs, adultes handicapés moteurs. Si vous avez 18 ans ou plus, venez vous joindre à nous. Aucune expérience dans l’aide aux personnes n’est requise, juste une certaine aptitude au contact humain. Vous aurez l’occasion de tisser des liens avec les participants, durant une dizaine de jours et de participer à une multitude d’activités intéressantes et délassantes.

Il est inutile d’insister sur l’importance de ces camps pour les campeurs. Bien au-delà du service rendu, ces camps représentent pour les aides une école de vie exceptionnelle, une expérience très enrichissante sur le plan humain.

Vous hésitez ? Allez lire les commentaires d’aides et de campeurs. Vous pouvez aussi contacter un responsable d’un des camps pour en savoir plus :

- camp de Bruxelles

- camp de Marbehan

- camp d’Oostduinkerke

- secrétariat de l’ABP

Une expérience enrichissante

Les aides, tous volontaires, sont chargés d’accompagner une personne handicapée pour le lever, les soins quotidiens, les repas, le coucher et les activités organisées par les responsables… mais aussi lors des sorties tant appréciées par tous : parc d’attractions, événements sportifs, etc.

Des rencontres inoubliables

Les trois camps ne se passent pas sous tente, mais bien dans des bâtiments adaptés. Selon le camp choisi, le nombre de « campeurs » handicapés varie de 10 à 50, de même que le nombre d’aides nécessaires.

Une super ambiance entre les jeunes bénévoles

Aucune qualification n’est requise, seul un esprit ouvert est indispensable. Vous serez entouré d’une équipe de bénévoles motivés et vous apprendrez rapidement grâce au soutien des anciens et des responsables de camp.

Témoignage : un aide a trouvé ... le bonheur

« Je viens rechercher… le bonheur au sein de ce camp. Pour moi, c’est vraiment pour cela que je viens. Je viens pour les campeurs. C’est ma principale motivation : des gens formidables avec qui on a un retour extraordinaire. Ce camp-ci permet de développer des sentiments que peut-être je n’avais jamais eus avec des gens qui apportent une certaine part de bonheur qu’il n’y a pas forcément au quotidien. On s’ouvre aux gens, on rit de bon coeur, on a vraiment un contact très très proche avec des gens qui sont … très vrais » (Michaël R.)

Nous avons besoin de vous et nous aimerions que vous aussi vous trouviez le bonheur !

