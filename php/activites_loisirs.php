Handivisit : Initiation à la voile
Avez-vous déjà eu envie de voir ce que ressentaient les skippers du Vendée Globe ? Vous êtes-vous déjà demandé comment un simple coup de vent pouvait faire accélérer un voilier et surtout, avez-vous déjà eu envie d’en prendre les commandes ? L’ABP a décidé de vous permettre de vous évader sur l’eau ce dimanche 22 avril 2018 avec une initiation à la voile !

Le club de Vilvoorde dispose de voiliers (appelés « caravelles ») adaptés pour permettre aux personnes avec une déficience de profiter d’un moment de détente sur l’eau, avec l’aide d’un skipper. En partenariat avec l’association Sailability, tout a été mis en place pour rendre l’expérience accessible à tous.

