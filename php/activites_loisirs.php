AVIRA : projet d'autopartage lancé par l'asbl Taxistop
Dans le cadre de son projet de voiture partagée, l’ABP est devenue membre d’AVIRA et bénéficie des conseils et du support de l’asbl Taxistop (initiatrice du projet AVIRA).
Le projet AVIRA consiste à partager des voitures adaptées PMR pour les rendre disponibles au plus grand nombre. Cela permettra donc, d’une part à ceux qui n’ont pas de véhicule adapté d’en utiliser un quand ils en ont besoin, et d’autre part, à ceux qui ont investi de l’argent dans un véhicule adapté de récupérer une partie des frais grâce au partage. Les véhicules peuvent appartenir aussi bien à des centres de soins, à des services résidentiels ou encore à des particuliers. Ils sont alors partagés avec le voisinage.

En 2013, Autopia (service d’autopartage de Taxistop), en collaboration avec l’asbl Pegode (une organisation qui fournit un soutien aux personnes handicapées), a initié un projet pilote d’autopartage accessible aux personnes moins valides. L’asbl Pegode est la première institution à partager ses véhicules adaptés entre ses clients résidents et les habitants des environs d’Edegem. Ce groupe d’autopartage porte le nom ’AdelE’ et compte aujourd’hui 32 utilisateurs (dont 16 voisins de l’asbl).

