La caravelle pouvant accueillir jusqu’à 4 personnes, n’hésitez pas à venir partager ce moment exceptionnel avec un proche, un collègue ou une connaissance.

Testé par notre équipe, l’accueil reçu au club et l’expérience sur l’eau vous permettront de totalement déconnecter le temps d’une journée. Nous espérons donc vous voir nombreux lors de cet après-midi qui regroupe détente, sport et rencontres.

