Conditions de prêt de la voiture
La mise à disposition d’un véhicule adapté partagé a déjà suscité de nombreuses demandes de renseignement. Il a été emprunté pour la première fois un WE fin octobre et sera à nouveau prêté dans le courant du mois de novembre pour une semaine. Voici, en quelques mots, les conditions pour le prêt de la voiture.

L’ABP est depuis peu propriétaire d’un véhicule transformé pour transporter une personne en chaise roulante. Cette voiture, en très bon état malgré son grand âge, nous a été offerte par une généreuse donatrice.

Pour qui ?

Ce véhicule est mis à disposition des personnes handicapées et de leur conducteur (ami, famille, …) à un prix réduit au coût de revient, et donc sans frais de chauffeur. Elles peuvent en bénéficier pour des courtes durées (une soirée, faire des courses, se rendre à une activité, etc.) ou de plus longues durées (vacances,…).

La priorité est donnée aux membres de l’ABP, pour le transport de personnes en chaise roulante, mais également au transport de personnes âgées ou porteuses d’un autre type de handicap.
Le véhicule bénéficie également à nos associations partenaires ou aux sympathisants de l’ABP.
Les autres associations ou particuliers peuvent l’emprunter à condition d’être membre de l’ABP (13€/an).

Les conditions pour le prêt de la voiture en quelques mots

Nous demandons une participation de 0,40 €/km (essence comprise). Cette somme couvre tous les frais : essence, entretien, assurance, taxes, etc.
Afin de ne pas bloquer le véhicule de longues durées pour une faible utilisation, un minimum de 25€/jour de location sera demandé. On paye donc 0,40€/km au-delà du 62ème kilomètre.
Exemples :
1) José emprunte la voiture 4 jours mais ne roule que 200 km sur les 4 jours. Etant donné qu’il n’a pas dépassé 62km x 4 jours (248km), il payera le forfait minimum : 25€ x 4 jours = 100€
2) Guy emprunte la voiture 4 jours et parcourt 300km. Il payera 300 x 0.4 = 120 €
Le forfait ne couvrant que les 248 premiers km pour 4 jours, il payera les 52 km suivants en supplément.

Si vous voulez emprunter régulièrement le véhicule il faudra fournir une photocopie du permis de conduire et compléter un formulaire pour l’assurance.

Equipement et dimensions

Le véhicule est équipé d’une boite automatique, de 4 sièges (y compris le siège conducteur), et d’un emplacement pour le transport d’une chaise roulante.

Afin de voir si votre chaise roulante rentre dans le véhicule, voici les dimensions de l’espace au sol réservé à la chaise roulante (entrée par l’arrière du véhicule).

