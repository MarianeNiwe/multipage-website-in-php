Fondés en 1949 par les associations « Association Belge des Paralysés » et « Auxilia », ils réunissent chaque année une centaine de personnes adultes, ayant un handicap physique relativement lourd, francophones et néerlandophones pour une dizaine de jours de vacances à la mer, dans les Ardennes ou à Bruxelles.

Encadrés par des équipes d’organisation et une centaine d’aides, tous bénévoles, les camps offrent l’occasion de réaliser une évasion dans une atmosphère de détente et de distractions pour des moins valides séjournant en home ou vivant isolés chez eux.

Dépendant en grande majorité d’une aide permanente, les campeurs sont accompagnés par des personnes bénévoles qui veillent à leurs soins quotidiens (lever, toilette, repas, …) et qui les secondent tout au long des activités organisées à leur intention. Idéalement, l’équipe organisatrice recherche un aidant par campeur.

Localisation des camps de vacances : les camps sont organisés actuellement
- à Bruxelles en juillet,
- à Marbehan en août et
- à Ter Helme (Oostduinkerke) en septembre.

Les vacanciers sont accueillis dans des hôtels ou des centres de vacances confortables et accessibles aux personnes en voiturette.

Programme des activités : comme ces vacances sont pour chacun une occasion de sortir de chez lui et de passer une période de détente en groupe, un soin particulier est apporté aux distractions proposées : visite d’expositions, shopping dans les magasins, excursions vers des centres de délassement ou des sites intéressants, animations variées et soirées dansantes, jeux de société sans oublier les spectacles de magie, de théâtre ou de chant.

Si vous êtes intéressé(e)s, prenez contact avec Marc Chaudoir de l’ABP (à l’adresse cvh@abpasbl.be - 02 421 69 64) ou avec un des responsables des camps dont les coordonnées sont reprises dans les rubriques de chaque camp ci-après.

Si vous voulez en savoir un peu plus sur le déroulement type d’un camp ou sur l’atmosphère particulière qui y règne, n’hésitez pas à lire le rapport 2011 sur le déroulement des camps !

Nous comptons sur vous et nous espérons partager avec vous notre prochain camp !

