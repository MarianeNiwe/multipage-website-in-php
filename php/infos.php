Centre de contact : nouveaux horaires d'accès et nouvelle organisation
Dès le 04 juin 2012, le contact center de la DG HAN 0800 987 99 sera accessible de 8h30 à 13h (et non plus jusqu’à 16h30). Il traitera en priorité les appels des personnes handicapées. Les professionnels disposeront de canaux supplémentaires, comme l’adresse e-mail ’HandiProF’

Le numéro 0800 : davantage destiné aux personnes handicapées

Pas moins de 45% des appelants sont des professionnels travaillant dans les CPAS, les communes, les mutualités, … Pour leur permettre d’atteindre son centre de contact, la DG HAN dispose désormais de canaux supplémentaires. Le centre de contact pourra ainsi, de 8 h 30 à 13 h, traiter en priorité les questions des personnes handicapées ou de leurs proches.

Les gestionnaires de dossiers de la DG HAN assisteront également le centre de contact en répondant de façon plus détaillée aux questions qui le nécessitent. Cela évitera que des personnes ne rappellent le centre de contact, après avoir reçu une réponse incomplète, lors d’un premier contact.

Une approche différente pour les professionnels

Désormais, les professionnels peuvent adresser leurs questions à la DG HAN en utilisant une nouvelle adresse e-mail : HandiProF@minsoc.fed.be.

Les collaborateurs du centre de contact les contacteront ensuite eux-mêmes pour répondre à ces questions. Ils le feront généralement à partir de 13 h, après les heures d’accessibilité du numéro 0800.

La DG HAN adaptera aussi son site Internet (www.handicap.fgov.be) à la nouvelle organisation : elle modifiera la rubrique « Contact » (en créant des pages séparées pour les personnes handicapées et pour les professionnels) et rédigera 10 nouvelles questions fréquemment posées.


