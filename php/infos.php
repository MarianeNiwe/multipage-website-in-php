Plate-forme Wallone "Bien vivre chez soi"
La Plate-forme "Bien vivre chez soi" vient de voir la jour en Wallonie.

Cette plate-forme s’inscrit dans le cadre des politiques en faveur des aînés et des personnes handicapées.

Elle a été mise en place par l’AWIPH et la Ministre wallonne de la Santé, de l’Action Sociale et de l’Egalité des chances, Madame Eliane Tillieux.

Ses objectifs sont les suivants :

- Permettre un accompagnement adéquat pour les personnes touchées directement ou non par des limitations dues à l’âge et/ou à un handicap.

- Contribuer à maintenir la personne à son domicile le plus longtemps possible en favorisant son indépendance et sa qualité de vie.

- Faire attention aux capacités et déficits de la personne ainsi qu’à ses habitudes, son projet de vie et son environnement.

Pour cela, 4 axes d’interventions sont prévus afin d’informer la population sur les services et aides existantes pour aménager, au mieux, son domicile.

- La premier axe, un site Internet
a été lancé ce 31 janvier 2012.
Il aura pour but d’informer sur les services et aides existantes afin de garder son autonomie à son domicile.

- Le second axe vise à proposer un conseil et un suivi en matière d’aménagement du domicile et d’aides techniques.

- Le troisième axe cible les programmes à mettre en place pour les plus de 65 ans en ce qui concerne les aides techniques et l’aménagement du domicile.

- Le quatrième axe, enfin, s’intéresse aux programmes de formation et d’échanges de bonnes pratiques pour les professionnels (des services de conseil en aménagement du domicile, notamment).

Nous vous tiendrons au courant des suites de cette plate-forme.



