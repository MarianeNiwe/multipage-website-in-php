Construire adaptable
Qu’est-ce donc que le logement adaptable ?

Nous savons tous que nous naissons PMR(*) et que nous mourrons PMR !

Voilà pourquoi la Confédération de la Construction Wallonne et le CAWaB, avec le soutien du fonds Social Européen et de la Région Wallonne ont développé un projet destiné à sensibiliser les constructeurs d’habitations sociales pour prendre en compte, dès le tracé des plans, le fait que les besoins des occupants vont évoluer.

D’où le concept de logement adaptable qui permettra de modifier à peu de frais le volume des pièces d’un appartement ou d’une maison - par simple suppression ou déplacement d’une ou de plusieurs cloisons - pour qu’une circulation en voiturette y soit aisée le jour cela sera utile ou nécessaire.

Une visite s’impose sur le site Construire adaptable

Téléchargez la brochure Construire Adaptable

(*) PMR = personne à mobilité réduite


