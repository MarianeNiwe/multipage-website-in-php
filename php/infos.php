  European Disability Card (EDC)
  Ce jeudi 19 octobre 2017 marque un tournant pour le secteur du handicap en Belgique.

  Il existe désormais une carte qui favorise l’accès des personnes en situation de handicap à la culture, au sport et aux loisirs (cinéma, théâtre, musée, parc d’attractions,...), offrant de nombreux avantages et ce, dans les 8 pays de l’Union Européenne participant : la Belgique, la Chypre, l’Estonie, la Finlande, l’Italie, Malte, la Slovénie et la Roumanie.

  Qui peut obtenir cette carte ?
  Toute personne qui est reconnue ou bénéficie d’une aide auprès d’une des 5 institutions belges chargées de mener la politique en matière d’intégration des personnes en situation de handicap.

  Comment et où utiliser cette carte ?
  Cette carte est strictement personnelle. Pour bénéficier des avantages, vous devez être en possession de la carte et la présenter lors de vos activités. Ses avantages vous sont exclusivement réservés.

  Les organisateurs d’activités sont libres de choisir les avantages octroyés. Voici quelques exemples :

  - une gratuité ou réduction du tarif pour la personne handicapée et/ou son accompagnant
  - un audio/visio guide gratuit
  - des visites guidées adaptées
  - des places accessibles ou résevées
  - un pass spécifique dans les parcs pour un accès plus aisé aux attractions

  Ceux-ci ne représentent qu’un échantillon des avantages possibles. Pour voir quels sont les avantages accordés dans les différents domaines (culture, sport, loisirs) aux personnes détenant la carte, vous pouvez consulter les sites d’informations, les publicités ou contacter directement le responsable du lieu où vous vous rendez.

  Comment obtenir cette carte ?
  Vous pouvez demander cette carte en vous rendant sur le site de la Direction Générale des Personnes Handicapées en cliquant ici.

  Pour obtenir plus d’informations sur la carte et les organisations qui acceptent celle-ci, vous pouvez également vous rendre sur le site dédié à l’European Disability Card : http://eudisabilitycard.be/fr .

