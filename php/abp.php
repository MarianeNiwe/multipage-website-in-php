<!-- HISTORIQUE -->
L’Association Belge des Paralysés existe depuis 1938.

Elle a quelques fleurons à son actif :

Dès le début des années 1950, elle a organisé des journées d’entraide artisanale, préfiguration des ateliers protégés.
Ensuite, elle s’est organisée pour déposer et enlever du travail au domicile des personnes bloquées chez elles par un handicap moteur.
En 1964, l’ABP a créé son atelier protégé, parmi les premiers de Belgique.
Celui-ci a grandi, s’est transformé en "ABP-Entreprise" en 1997.
Il a fusionné avec Manufast en 2005, formant actuellement Manufast-ABP, une Entreprise de Travail Adapté - ETA de plus de 300 personnes dont 250 travailleurs handicapés.
Par ailleurs, l’ABP s’est soucié très tôt de sortir les handicapés moteur de chez eux.

Ce fut d’abord à l’occasion des camps de vacances qui commencèrent dès 1949.

Ce fut, ensuite, via de très nombreuses activités récréatives, sociales, musicales, artistiques ou, simplement touristiques, dans les centres provinciaux et les sections locales.

Le service social de l’ABP a compté jusqu’à une dizaine d’assistantes sociales actives dans tout le pays. L’évolution institutionnelle et la création de nombreuses autres associations ont réduit nos ambitions mais, à ce jour, le service social de l’ABP a toujours sa raison d’être.
Beaucoup d’activités menées par l’ABP ont été reprises par des associations locales, avec les mêmes objectifs, souvent avec les mêmes personnes mais sous d’autres noms.

De ces presque 80 ans d’existence active, il reste encore les activités provinciales de Boussu et de Rixensart qui maintiennent, chaque mois, l’esprit et l’image de l’ABP.

Depuis janvier 2013 et sa fusion avec l’Association Francophone Polio & Post-Polio, l’ABP a renforcé la représentation des personnes ayant été atteintes de la polio. Elle les informe et les rassemble lors des sessions d’information sur l’évolution du syndrome post-polio et les séquelles de cette maladie.

Pour en savoir plus, une large présentation de l’historique a été réalisée dansl’ABP Contact de décembre 2013
