Qui sommes-nous ?
Située à Bruxelles, dans les locaux de l’Entreprise de Travail Adapté (ETA) Manufast-ABP, l’ABP oeuvre, au quotidien, au sein d’organes clés en matière d’accessibilité et de handicap comme :
- le Collectif Accessibilité Wallonie-Bruxelles (CAWaB)
- la Commission Régionale de Mobilité (CRM) - section PMR
- le Conseil Consultatif de la COCOF

Parmi les priorités de l’ABP : le service d’informations sociales.
Ce service a pour but de simplifier la vie des membres qui souhaitent un renseignement ou veulent être dirigés vers une administration ou un service spécialisé. On a par ailleurs tenté de rassembler, sur ce site, plusieurs rubriques vous permettant de trouver réponse à vos questions.
Si tel n’est pas le cas, appelez-nous au 02 / 421 69 64.
Nous disposons également de matériel que nous mettons volontiers à disposition de nos membres

Une de nos activités-phares : les camps de vacances pour handicapés (moteur) - CVH. Au nombre de trois à l’heure actuelle, ils existent depuis plus de 65 ans. Des images valent mieux que de grands discours, alors regardez le film ci-dessous ...


